print("Suma:\n",a," + ",b," = ", suma)
print("Resta:\n",a," - ",b," = ", resta)
print("Multiplicación:\n",a," * ",b," = ", multiplicacion)
print("División:\n",a," / ",b," = ", division)
print("Modulo:\n",a," % ",b," = ", modulo)
print("potencia:\n",a," ** ",b," = ", potencia)


